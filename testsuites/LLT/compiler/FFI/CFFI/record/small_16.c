{
    return b0 && b1 && b2 && b3 && b4 && b5 && b6 && b7 && b8 && b9 && b10;
}

bool X_Or(bool b0, bool b1, bool b2, bool b3, bool b4, bool b5, bool b6, bool b7, bool b8, bool b9, bool b10)
{
    return b0 || b1 || b2 || b3 || b4 || b5 || b6 || b7 || b8 || b9 || b10;
}
